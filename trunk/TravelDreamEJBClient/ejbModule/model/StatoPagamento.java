package model;

public class StatoPagamento {
	
	private String Viaggio;
	private String Albergo;
	private String Escursioni;
	public String getViaggio() {
		return Viaggio;
	}
	public void setViaggio(String viaggio) {
		Viaggio = viaggio;
	}
	public String getAlbergo() {
		return Albergo;
	}
	public void setAlbergo(String albergo) {
		Albergo = albergo;
	}
	public String getEscursioni() {
		return Escursioni;
	}
	public void setEscursioni(String escursioni) {
		Escursioni = escursioni;
	}

}
